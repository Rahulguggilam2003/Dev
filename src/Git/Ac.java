package Git;
import java.util.*;
public class Ac {
	//area of circle,rectangle,triangle function names,ac,rec,tri
	public double Ac(int rad) {
		return 3.14*rad*rad;
	}

	public int Rec(int x,int y) {
		return x*y;
	}
	public double tri(int x,int y) {
		return 0.5*x*y;
	}
}



